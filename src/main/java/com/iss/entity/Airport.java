package com.iss.entity;

public class Airport {
    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getFrcd() {
        return frcd;
    }

    public void setFrcd(String frcd) {
        this.frcd = frcd;
    }

    public String getApat() {
        return apat;
    }

    public void setApat(String apat) {
        this.apat = apat;
    }

    public String getCnnm() {
        return cnnm;
    }

    public void setCnnm(String cnnm) {
        this.cnnm = cnnm;
    }

    public String getEnnm() {
        return ennm;
    }

    public void setEnnm(String ennm) {
        this.ennm = ennm;
    }

    public String getAiso() {
        return aiso;
    }

    public void setAiso(String aiso) {
        this.aiso = aiso;
    }

    public String getApsn() {
        return apsn;
    }

    public void setApsn(String apsn) {
        this.apsn = apsn;
    }

    String code;
    String frcd;
    String apat;
    String cnnm;
    String ennm;
    String aiso;
    String apsn;
}
